    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        num2=Double.parseDouble(txtPantalla.getText());
        switch(operacion){
            case 1:
                result = operaAritmetica.sumar(num1, num2);
                limpiarVariables();
                break;
                
            case 2:
                 result = operaAritmetica.restar(num1, num2);
                limpiarVariables();
                break;
            case 3:
                result = operaAritmetica.dividir(num1, num2);
                limpiarVariables();
                break;
                
            case 4:
                result = operaAritmetica.multiplicar(num1, num2);
                limpiarVariables();
                break;
                
            case 5:
                result = operaCalc.potencia(num1, num2);
                limpiarVariables();                
                break;               
        }
        txtPantalla.setText(String.valueOf(result));
        result = 0.0;
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void btnRaizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRaizActionPerformed
        // TODO add your handling code here:
        num1 = Double.parseDouble(txtPantalla.getText());
        result=operaCalc.raiz(num1);
        limpiarVariables();
        txtPantalla.setText(String.valueOf(result));
        result = 0.0;
        
    }//GEN-LAST:event_btnRaizActionPerformed

