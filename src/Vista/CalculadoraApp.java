      public void escribeNum(int num ){
            if(txtPantalla.getText().equals("0")){
                txtPantalla.setText("");

              }
            txtPantalla.setText(txtPantalla.getText()+ String.valueOf(num));
        }
    
      public void limpiarVariables(){
          num1=0.0;
          num2=0.0;
          txtPantalla.setText("0");
          operacion=0;
          
      }
